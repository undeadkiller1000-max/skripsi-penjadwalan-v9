# 7. EDD BASELINE (Earliest Due Date — dispatching rule murni)
# [FIX-3] Tambahan baseline ketiga untuk perbandingan
# ============================================================
def run_edd_baseline(jobs, P_dict, D_dict, W_dict, R_dict):
    """
    Evaluasi jadwal EDD murni (tanpa iterasi optimasi).
    Urutan: diurutkan ascending berdasarkan due date, tidak diubah.
    """
    edd_seq    = sorted(jobs, key=lambda x: D_dict[x])
    m_avail    = {m: 0.0 for m in STATIONS}
    j_avail    = {j: R_dict[j] for j in jobs}   # [FIX-1] release date
    total_tard = 0
    sched      = []

    for j in edd_seq:
        rute = [m for m in STATIONS if P_dict[j][m] > 0]
        for m in rute:
            dur   = P_dict[j][m]
            start = max(m_avail[m], j_avail[j])
            end   = start + dur
            m_avail[m] = end
            j_avail[j] = end
            sched.append({'job': j, 'm': m, 'start': start, 'dur': dur})
        total_tard += max(0, j_avail[j] - D_dict[j]) * W_dict[j]

    return total_tard, sched, j_avail


