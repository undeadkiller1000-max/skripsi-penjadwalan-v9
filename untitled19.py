# 10. INSERTION ESTIMATOR — Fitur Baru
# Joint re-optimization: locked jobs dipertahankan posisinya,
# free jobs + new jobs di-reschedule ulang bersama.
# ============================================================
def estimasi_order_baru(
    new_jobs_data,      # list of dict: data order baru (sama format dengan jobs_raw)
    existing_job_ids,   # list semua job ID di jadwal existing
    locked_job_ids,     # set job ID yang dikunci
    existing_schedule,  # dict {job_id: {stasiun: start_time}} dari jadwal winning
    P_dict_existing,    # dict Pᵢₘ untuk existing jobs
    D_dict_existing,    # dict due date untuk existing jobs
    W_dict_existing,    # dict bobot untuk existing jobs
    R_dict_existing,    # dict release date untuk existing jobs
    resources,
    setup_time,
    start_date,
    priority_weight,
    milp_time_limit,
):
    """
    Estimasi earliest completion time untuk order baru sambil menjaga
    order yang terkunci tidak terlambat.

    Pendekatan: Joint re-optimization (Pendekatan B)
    - Locked jobs: posisi dipertahankan dengan hard constraint di MILP
    - Free (unlocked) jobs + new jobs: di-reschedule ulang bersama
    - Jaminan: locked jobs TIDAK bisa terlambat (constraint ketat)
    """
    # Hitung Pᵢₘ untuk order baru
    new_job_ids = []
    P_new       = {}
    D_new       = {}
    W_new       = {}
    R_new       = {}

    for nj in new_jobs_data:
        jid       = str(nj['id pesanan'])
        new_job_ids.append(jid)
        P_new[jid] = hitung_waktu_proses(nj, resources, setup_time)

        # Due date order baru: gunakan tanggal yang diberikan user
        target_dt   = nj['due date (tanggal)'].replace(hour=17, minute=30, second=0, microsecond=0)
        D_new[jid]  = hitung_target_menit(target_dt, start_date)
        W_new[jid]  = priority_weight if nj.get('Priority', False) else 1
        # Release date order baru = saat estimasi dilakukan = start dari horizon
        R_new[jid]  = hitung_target_menit(
            nj.get('release_dt', start_date).replace(hour=8, minute=30, second=0, microsecond=0),
            start_date
        ) if 'release_dt' in nj else 0

    # Gabungkan semua job
    all_ids   = existing_job_ids + new_job_ids
    P_all     = {**P_dict_existing, **P_new}
    D_all     = {**D_dict_existing, **D_new}
    W_all     = {**W_dict_existing, **W_new}
    R_all     = {**R_dict_existing, **R_new}

    locked_sched_map = {}
    for lj in locked_job_ids:
        if lj in existing_schedule:
            locked_sched_map[lj] = existing_schedule[lj]

    # ── SA untuk warm start ──────────────────────────────────
    sa_score, sa_sched, sa_end = run_simulated_annealing(
        all_ids, P_all, D_all, W_all, R_all,
        locked_jobs=locked_job_ids,
        locked_schedule=locked_sched_map,
    )

    # ── MILP dengan hard constraint untuk locked jobs ────────
    milp_score    = float('inf')
    milp_feasible = False
    milp_sched    = []
    milp_end      = {}
    gap_pct       = None

    if len(all_ids) >= 2:
        prob     = pulp.LpProblem("Insertion_JobShop", pulp.LpMinimize)
        S        = pulp.LpVariable.dicts("S",    (all_ids, STATIONS), lowBound=0, cat='Continuous')
        Tard_var = pulp.LpVariable.dicts("Tard", all_ids,             lowBound=0, cat='Continuous')

        # BigM per-mesin (ketat)
        BigM_per_mesin = {}
        for m in STATIONS:
            total_dur = sum(P_all[i][m] for i in all_ids if P_all[i][m] > 0)
            BigM_per_mesin[m] = max(total_dur, 1.0)

        # Variabel biner hanya untuk pasangan yang share mesin
        Y = {}
        for m in STATIONS:
            aktif = [i for i in all_ids if P_all[i][m] > 0]
            for a in range(len(aktif)):
                for b in range(a + 1, len(aktif)):
                    i, j = aktif[a], aktif[b]
                    vname = f"Y_{safe_var_name(i)}_{safe_var_name(j)}_{safe_var_name(m)}"
                    Y[(i, j, m)] = pulp.LpVariable(vname, cat='Binary')

        # Objektif: minimize weighted tardiness semua job
        prob += pulp.lpSum(W_all[i] * Tard_var[i] for i in all_ids)

        for i in all_ids:
            rute = [m for m in STATIONS if P_all[i][m] > 0]

            # [FIX-1] Release date constraint
            if rute:
                prob += S[i][rute[0]] >= R_all[i]

            # Presedensi
            for k in range(1, len(rute)):
                prob += S[i][rute[k]] >= S[i][rute[k-1]] + P_all[i][rute[k-1]]

            # Tardiness
            if rute:
                prob += Tard_var[i] >= (S[i][rute[-1]] + P_all[i][rute[-1]]) - D_all[i]

            # [KEY] Hard constraint: locked jobs — posisi tidak bergerak
            if i in locked_job_ids and i in locked_sched_map:
                for m, fixed_start in locked_sched_map[i].items():
                    if P_all[i].get(m, 0) > 0:
                        prob += S[i][m] == fixed_start
                # Tambahan: locked job harus TEPAT WAKTU (Tardiness = 0)
                prob += Tard_var[i] == 0

        # No-overlap per mesin
        for (i, j, m), y_var in Y.items():
            bm = BigM_per_mesin[m]
            prob += S[j][m] >= S[i][m] + P_all[i][m] - bm * y_var
            prob += S[i][m] >= S[j][m] + P_all[j][m] - bm * (1 - y_var)

        # Warm-start dari SA
        sa_start_map = {(e['job'], e['m']): e['start'] for e in sa_sched}
        for i in all_ids:
            for m in STATIONS:
                val = sa_start_map.get((i, m))
                if val is not None:
                    try:
                        S[i][m].setInitialValue(val)
                    except Exception:
                        pass
        for (i, j, m), y_var in Y.items():
            si = sa_start_map.get((i, m))
            sj = sa_start_map.get((j, m))
            if si is not None and sj is not None:
                try:
                    y_var.setInitialValue(1 if sj < si else 0)
                except Exception:
                    pass

        prob.solve(pulp.PULP_CBC_CMD(timeLimit=milp_time_limit, msg=0, warmStart=True))

        milp_status   = pulp.LpStatus[prob.status]
        obj_val       = pulp.value(prob.objective)
        milp_feasible = milp_status in ('Optimal', 'Feasible') and obj_val is not None
        milp_score    = float(obj_val) if milp_feasible else float('inf')

        # [FIX-2] Optimality gap
        if milp_feasible:
            best_bound = getattr(prob, 'bestBound', None)
            if best_bound is not None and milp_score > 0:
                gap_pct = abs(milp_score - float(best_bound)) / (abs(milp_score) + 1e-10) * 100
            else:
                gap_pct = 0.0 if milp_status == 'Optimal' else None

            for i in all_ids:
                rute = [m for m in STATIONS if P_all[i][m] > 0]
                if rute:
                    milp_end[i] = (S[i][rute[-1]].varValue or 0) + P_all[i][rute[-1]]
                else:
                    milp_end[i] = 0
                for m in rute:
                    milp_sched.append({
                        'job'  : i,
                        'm'    : m,
                        'start': round(S[i][m].varValue or 0, 2),
                        'dur'  : P_all[i][m],
                    })

    # Pilih solusi terbaik
    if milp_feasible and milp_score <= sa_score:
        final_sched = milp_sched
        final_end   = milp_end
        method_used = f"MILP (gap: {gap_pct:.2f}%)" if gap_pct is not None else "MILP"
        final_score = milp_score
    else:
        final_sched = sa_sched
        final_end   = sa_end
        method_used = "Simulated Annealing"
        final_score = sa_score

    return {
        'sched'        : final_sched,
        'end_times'    : final_end,
        'method'       : method_used,
        'score'        : final_score,
        'gap_pct'      : gap_pct,
        'new_job_ids'  : new_job_ids,
        'P_new'        : P_new,
        'D_new'        : D_new,
    }


