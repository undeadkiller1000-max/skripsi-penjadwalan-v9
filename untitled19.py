# 6. SIMULATED ANNEALING
# Release date diimplementasikan: start = max(m_avail, j_avail, R[j])
# ============================================================
def run_simulated_annealing(jobs, P_dict, D_dict, W_dict, R_dict,
                             locked_jobs=None, locked_schedule=None):
    """
    SA untuk Weighted Total Tardiness Minimization pada Job Shop.
    - jobs          : list semua job ID yang akan dijadwalkan
    - R_dict        : release date per job (menit efektif)
    - locked_jobs   : set job ID yang posisinya dikunci (tidak ikut permutasi)
    - locked_schedule: dict {job_id: {stasiun: start_time}} untuk job terkunci
    """
    if locked_jobs is None:
        locked_jobs = set()
    if locked_schedule is None:
        locked_schedule = {}

    free_jobs   = [j for j in jobs if j not in locked_jobs]
    locked_list = [j for j in jobs if j in locked_jobs]

    def eval_seq(free_seq):
        """Evaluasi sekuens: locked jobs diinjeksikan ke jadwal berdasarkan posisi fixednya."""
        m_avail = {m: 0.0 for m in STATIONS}
        j_avail = {}

        # Inisialisasi locked jobs ke m_avail berdasarkan jadwal existing
        for lj in locked_list:
            lj_end = 0
            for m in STATIONS:
                if lj in locked_schedule and m in locked_schedule[lj]:
                    s  = locked_schedule[lj][m]
                    dur = P_dict[lj][m]
                    e   = s + dur
                    # Blokir mesin sampai locked job selesai
                    m_avail[m] = max(m_avail[m], e)
                    lj_end = max(lj_end, e)
            j_avail[lj] = lj_end

        total_tard = 0
        sched      = []

        # Tambahkan tugas locked ke sched untuk visualisasi
        for lj in locked_list:
            if lj in locked_schedule:
                for m, s in locked_schedule[lj].items():
                    dur = P_dict[lj].get(m, 0)
                    if dur > 0:
                        sched.append({'job': lj, 'm': m, 'start': s, 'dur': dur})
            tard_lj = max(0, j_avail.get(lj, 0) - D_dict[lj]) * W_dict[lj]
            total_tard += tard_lj

        # Jadwalkan free jobs dalam urutan free_seq
        for j in free_seq:
            rute       = [m for m in STATIONS if P_dict[j][m] > 0]
            j_avail[j] = R_dict[j]   # [FIX-1] release date constraint
            for m in rute:
                dur   = P_dict[j][m]
                start = max(m_avail[m], j_avail[j])
                end   = start + dur
                m_avail[m] = end
                j_avail[j] = end
                sched.append({'job': j, 'm': m, 'start': start, 'dur': dur})
            total_tard += max(0, j_avail[j] - D_dict[j]) * W_dict[j]

        return total_tard, sched, j_avail

    def swap(seq):
        s = seq.copy()
        if len(s) < 2:
            return s
        a, b = random.sample(range(len(s)), 2)
        s[a], s[b] = s[b], s[a]
        return s

    def insert(seq):
        s = seq.copy()
        if len(s) < 2:
            return s
        a   = random.randrange(len(s))
        job = s.pop(a)
        b   = random.randrange(len(s) + 1)
        s.insert(b, job)
        return s

    # Inisialisasi dengan EDD
    cur_seq    = sorted(free_jobs, key=lambda x: D_dict[x])
    cur_score, _, _ = eval_seq(cur_seq)
    best_seq   = cur_seq.copy()
    best_score = cur_score

    T_sa    = 500.0
    cooling = 0.997
    n_iter  = 8000

    for _ in range(n_iter):
        new_seq   = swap(cur_seq) if random.random() < 0.7 else insert(cur_seq)
        new_score, _, _ = eval_seq(new_seq)
        delta     = new_score - cur_score

        if delta < 0 or (T_sa > 1e-10 and random.random() < math.exp(-delta / T_sa)):
            cur_seq   = new_seq
            cur_score = new_score
            if new_score < best_score:
                best_seq   = new_seq.copy()
                best_score = new_score

        T_sa *= cooling

    _, final_sched, final_end = eval_seq(best_seq)
    return best_score, final_sched, final_end


